namespace bucstopapi.Enums
{
    /// <summary>
    /// Represents the available game attributes that can be retrieved.
    /// </summary>
    public enum GameAttributes
    {
        /// <summary>
        /// The description of the game.
        /// </summary>
        Description,

        /// <summary>
        /// The code of the game.
        /// </summary>
        GameCode,

        /// <summary>
        /// The generic information about the game.
        /// </summary>
        GameInfo,

        /// <summary>
        /// The instructions on how to play the game.
        /// </summary>
        HowToPlay,

        /// <summary>
        /// The leaderboard of the game.
        /// </summary>
        Leaderboard,

        /// <summary>
        /// The thumbnail of the game.
        /// </summary>
        Thumbnail
    }
}
